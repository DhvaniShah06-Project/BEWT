const mongoose=require('mongoose')
const userSchema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type: String,
        required: true,
        unique: true  
    },
    createdAt: {
         type: Date,
        default: Date.now
  },
  role:{
        type:String
  }
})




module.exports=mongoose.model("User",userSchema)




